const regular = /^(#|＃|\/)?取消[1-9]d*/;
var res = onResponse(selects, async (e) => {
    const Send = useSend(e);
    const usr_qq = e.UserId;
    const ifexistplay = await existplayer(usr_qq);
    if (!ifexistplay)
        return false;
    let Forum = [];
    const player = await readPlayer(usr_qq);
    const x = parseInt(e.MessageText.replace(/^(#|＃|\/)?取消/, '')) - 1;
    try {
        Forum = await readForum();
    }
    catch {
        await writeForum([]);
    }
    if (x >= Forum.length) {
        Send(Text(`没有编号为${x + 1}的宝贝需求`));
        return false;
    }
    if (Forum[x].qq != usr_qq) {
        Send(Text('不能取消别人的宝贝需求'));
        return false;
    }
    await addCoin(usr_qq, Forum[x].whole);
    Send(Text(player.名号 +
        '取消' +
        Forum[x].name +
        '成功,返还' +
        Forum[x].whole +
        '灵石'));
    Forum.splice(x, 1);
    await writeForum(Forum);
});

export { res as default, regular };
